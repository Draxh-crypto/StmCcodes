int add(int,int);
int sub(int,int);
long int mul(int,int);
float div(int,int);

int add(int a,int b){

return a+b;

}
int sub(int a,int b){

	return a-b;

}
long int mul(int a,int b){

	return a*b;

}
float div(int a,int b){

	return (float)a/b;

}






